// Kütüphaneler
#include <stdio.h>

// main
int main(void)
{
    int c = 1427; // c ye başlangıç değeri atanması
    double p = 1427.0; // p ye başlangıç değeri atanması

    printf("%#o\n", c);
    printf("%#x\n", c);
    printf("%#X\n", c);
    printf("\n%g\n", p);
    printf("%#g\n", p);
}